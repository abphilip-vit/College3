ORG 0000H
MOV P0, 00000000B   // INPUTS - A,B
MOV P1, 00000000B   // OUTPUTS - C,D
XX: MOV C,P0.1
	ANL C,P0.0
	MOV 00H,C       
	MOV C,P0.0
	MOV 04H,C
	CPL 04H
	ANL C,04H
	MOV 01H,C		// BB'
	MOV C,04H
	ANL C,P0.1 		
	MOV 02H,C       // AB'
	MOV C,01H
	ORL C,02H
	MOV P1.1,C		// AB'+BB' = C
	MOV C,00H
	MOV P1.0,C		// AB = D
	SJMP XX
END